#include "libft.h"

char	*ft_substr(char *s, int start, int len)
{
	char	*str;
	int		x;

	if (!s)
		return (NULL);
	if (ft_strlen(s) < start)
		len = 0;
	if (ft_strlen(s) - start < len)
		len = ft_strlen(s + start);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	x = 0;
	while (s[x] && x < len)
	{
		str[x] = s[start];
		start++;
		x++;
	}
	str[x] = '\0';
	return (str);
}
